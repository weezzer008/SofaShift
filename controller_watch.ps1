        topo[0].sourceInfo.adapterId.LowPart = adapterLow;
        topo[0].sourceInfo.adapterId.HighPart = adapterHigh;
        topo[0].sourceInfo.modeInfoIdx = 0;
        topo[0].targetInfo.modeInfoIdx = DISPLAYCONFIG_PATH_MODE_IDX_INVALID;
