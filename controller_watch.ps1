function Handle-Connect {
    param([string]$DeviceId)

    # Look up profile by instance path first, then container ID fallback.
    $targetProfile = $global:CanonicalIdToProfile[$DeviceId]
    Write-Log "Connected: $DeviceId  =>  $targetProfile"

    # Reconnected within debounce window.
    # Only cancel cleanup silently if the reconnecting controller maps to the CURRENTLY-ACTIVE
    # profile. If it maps to a DIFFERENT profile (user grabbed a second controller), we still
    # need to switch, so fall through to the normal handling below.
    if ($global:OfflineStartTime -ne $null) {
        $elapsed = (Get-Date) - $global:OfflineStartTime
        if ($elapsed.TotalSeconds -lt $cfg_DebounceSeconds) {
            if ($targetProfile -and $targetProfile -eq $global:ActiveProfile) {
                Write-Log "Reconnect within debounce window - cancelling cleanup"
                $global:OfflineStartTime = $null
                return
            } else {
                Write-Log "Reconnect within debounce window - different profile '$targetProfile', applying switch"
                $global:OfflineStartTime = $null
                # fall through to normal switch
            }
        }
    }

    # First controller wins - once any profile is active, ignore additional controller
    # connections until all controllers disconnect and cleanup fires (setting ActiveProfile=null).
    if ($global:ActiveProfile -ne $null) {
        Write-Log "Profile '$global:ActiveProfile' already active - ignoring '$DeviceId' (first controller wins)"
        return
    }

    $global:ActiveProfile    = $targetProfile
    $global:OfflineStartTime = $null
    Invoke-DisplayProfile -ProfileName $targetProfile

    $activeProfileData = $cfg_Profiles[$targetProfile]
    $usePlaynite = if ($activeProfileData -and $activeProfileData.ContainsKey('UsePlaynite')) { [bool]$activeProfileData['UsePlaynite'] } else { $true }
    $useHueSync  = if ($activeProfileData -and $activeProfileData.ContainsKey('UseHueSync'))  { [bool]$activeProfileData['UseHueSync']  } else { $true }

    if ($usePlaynite) {
        if ($cfg_Playnite -and (Test-Path $cfg_Playnite)) {
            Write-Log "Launching Playnite"
            Register-LaunchedProcess -Label "Playnite" -Path $cfg_Playnite -ProcessNames @("Playnite.FullscreenApp","Playnite.DesktopApp")
            Start-Process -FilePath $cfg_Playnite
        } elseif ($cfg_Playnite) {
            Write-Log "WARNING: Playnite not found: $cfg_Playnite"
        }
    } else {
        Write-Log "Playnite skipped for profile '$targetProfile'"
    }

    if ($useHueSync) { Start-HueSync } else { Write-Log "Hue Sync skipped for profile '$targetProfile'" }

    $useCustomApps = $null
    if ($activeProfileData -and $activeProfileData.ContainsKey('UseCustomApps')) { $useCustomApps = @($activeProfileData['UseCustomApps']) }
    $useCustomApp = if ($activeProfileData -and $activeProfileData.ContainsKey('UseCustomApp')) { [bool]$activeProfileData['UseCustomApp'] } else { $true }
    if ($useCustomApps -and $useCustomApps.Count -gt 0) {
        if ($useCustomApps -contains $true) { Start-CustomApp $useCustomApps } else { Write-Log "Custom apps skipped for profile '$targetProfile'" }
    } elseif ($useCustomApp) {
        Start-CustomApp
    } else {
        Write-Log "Custom app skipped for profile '$targetProfile'"
    }

    Start-Sleep -Milliseconds 400
    Invoke-MinimizeAll
    Invoke-DismissOSK
    if (-not (Focus-Window -TitleRegex $cfg_FocusTarget)) {
        Start-Sleep -Milliseconds 400
        Invoke-DismissOSK
        [void](Focus-Window -TitleRegex $cfg_FocusTarget)
    }
}

function Handle-Disconnect {
    param([string]$DeviceId)
    Write-Log "Disconnected: $DeviceId"
    # OfflineStartTime is set in the main loop when $anyPresent flips to false
}

function Invoke-DisconnectCleanup {
    Write-Log "No controllers for $cfg_DebounceSeconds s - running cleanup"

    Stop-Playnite
    Stop-HueSync
    Stop-CustomApp
    Invoke-DisplayProfile -ProfileName $cfg_DesktopProfile

    $global:ActiveProfile    = $null
    $global:OfflineStartTime = $null
    Write-Log "Cleanup complete"
}

# ============================================================
# Main polling loop
# ============================================================
while ($true) {
    $presentIds = Get-PresentControllerIds
    $anyPresent = ($presentIds.Count -gt 0)

    # Edge: newly connected
    foreach ($id in $presentIds) {
        if ($global:LastPresentIds -notcontains $id) {
            Handle-Connect -DeviceId $id
        }
    }

    # Edge: newly disconnected
    foreach ($id in $global:LastPresentIds) {
        if ($presentIds -notcontains $id) {
            Handle-Disconnect -DeviceId $id
        }
    }

    # Track when all controllers went offline (debounce start)
    if ($anyPresent) {
        $global:OfflineStartTime = $null
    } elseif ($global:LastAnyPresent -and -not $anyPresent) {
        $global:OfflineStartTime = Get-Date
        Write-Log "All controllers offline - starting $cfg_DebounceSeconds s debounce"
    }

    # Fire cleanup once debounce period expires
    if (-not $anyPresent -and $global:OfflineStartTime -ne $null) {
        $elapsed = (Get-Date) - $global:OfflineStartTime
        if ($elapsed.TotalSeconds -ge $cfg_DebounceSeconds) {
            Invoke-DisconnectCleanup
        }
    }

    $global:LastPresentIds = $presentIds
    $global:LastAnyPresent = $anyPresent

    Start-Sleep -Milliseconds $script:PollMs
}












