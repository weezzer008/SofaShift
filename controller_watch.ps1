# ============================================================
# Normalize watched controller IDs once at startup.
# Polling filters in PowerShell instead of building a WMI query,
# which avoids "Invalid query" failures from escaped device paths.
# ============================================================
$global:WatchedControllerIds = [System.Collections.Generic.HashSet[string]]::new([System.StringComparer]::OrdinalIgnoreCase)
$global:WatchedContainerIds  = [System.Collections.Generic.HashSet[string]]::new([System.StringComparer]::OrdinalIgnoreCase)
# Canonical ID -> profile name.  Keys are either HID instance paths (from cfg_ControllerMap)
# or Container GUIDs (from cfg_ControllerContainerMap).  Container IDs are used when the
# Xbox wireless adapter assigns a different instance path to the same physical controller
# depending on how many controllers are connected (slot assignment is volatile).
$global:CanonicalIdToProfile = @{}

foreach ($id in $cfg_ControllerMap.Keys) {
    if (-not [string]::IsNullOrWhiteSpace([string]$id)) {
        $trimmed = ([string]$id).Trim()
        [void]$global:WatchedControllerIds.Add($trimmed)
        $global:CanonicalIdToProfile[$trimmed] = $cfg_ControllerMap[$id]
    }
}

# Load container ID map if the config has one (generated by wizard >= this version).
if (Get-Variable cfg_ControllerContainerMap -Scope Global -ErrorAction SilentlyContinue) {
    foreach ($cid in $cfg_ControllerContainerMap.Keys) {
        if (-not [string]::IsNullOrWhiteSpace([string]$cid)) {
            $trimmed = ([string]$cid).Trim()
            [void]$global:WatchedContainerIds.Add($trimmed)
            $global:CanonicalIdToProfile[$trimmed] = $cfg_ControllerContainerMap[$cid]
        }
    }
}

Write-Log "Watching $($global:WatchedControllerIds.Count) mapped controller ID(s)$(if ($global:WatchedContainerIds.Count -gt 0) { " + $($global:WatchedContainerIds.Count) container ID(s) for slot-stable matching" })"


