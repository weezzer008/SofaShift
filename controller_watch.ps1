Set-StrictMode -Off
$ErrorActionPreference = "Continue"

# ============================================================
# Logging
# ============================================================
$global:LogFile = "$PSScriptRoot\controller_watch.log"

# Rotate log if it grows past ~1 MB.  Keeps one previous generation.
try {
    if (Test-Path -LiteralPath $global:LogFile) {
        $sz = (Get-Item -LiteralPath $global:LogFile).Length
        if ($sz -gt 1MB) {
            $old = "$global:LogFile.old"
            if (Test-Path -LiteralPath $old) { Remove-Item -LiteralPath $old -Force -ErrorAction SilentlyContinue }
            Move-Item -LiteralPath $global:LogFile -Destination $old -Force -ErrorAction SilentlyContinue
        }
    }
} catch {}

function Write-Log {
    param([string]$Msg)
    $line = "$(Get-Date -Format 'yyyy-MM-dd HH:mm:ss.fff')  $Msg"
    Add-Content -Path $global:LogFile -Value $line -Encoding UTF8
    Write-Host $line
}

