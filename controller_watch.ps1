    static void Query(out DISPLAYCONFIG_PATH_INFO[] paths, out DISPLAYCONFIG_MODE_INFO[] modes)
    {
        uint pathCount, modeCount;
        int rc;
        do {
            rc = GetDisplayConfigBufferSizes(QDC_ALL_PATHS, out pathCount, out modeCount);
            if (rc != 0) throw new InvalidOperationException("GetDisplayConfigBufferSizes failed: " + rc);
            paths = new DISPLAYCONFIG_PATH_INFO[pathCount];
            modes = new DISPLAYCONFIG_MODE_INFO[modeCount];
            rc = QueryDisplayConfig(QDC_ALL_PATHS, ref pathCount, paths, ref modeCount, modes, IntPtr.Zero);
            if (rc == 122) continue;
            if (rc != 0) throw new InvalidOperationException("QueryDisplayConfig failed: " + rc);
            if (pathCount != paths.Length) Array.Resize(ref paths, (int)pathCount);
            if (modeCount != modes.Length) Array.Resize(ref modes, (int)modeCount);
            break;
