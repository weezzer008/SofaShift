    // Applies refresh rate (and optionally resolution) to a GDI device via ChangeDisplaySettingsEx.
    // Pass width=0 / height=0 to keep the current resolution and only change Hz.
    // Returns 0 on success (DISP_CHANGE_SUCCESSFUL).
    public static int SetRefreshRate(string gdiDeviceName, uint width, uint height, uint hz)
    {
        if (string.IsNullOrEmpty(gdiDeviceName) || hz == 0) return -1;
        var dm = new DEVMODE();
        dm.dmSize = (short)Marshal.SizeOf(typeof(DEVMODE));
