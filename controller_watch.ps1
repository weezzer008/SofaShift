                    $script:LastContainerScanUtc = $now
                    $containerPresent = [System.Collections.Generic.List[string]]::new()

                    # Container IDs are only needed for Xbox wireless adapter slot changes.
                    # Limit the scan to HID devices so the steady-state poll does not walk
                    # every PnP device on the machine.
                    $hidDevices = @(Get-PnpDevice -PresentOnly -Class HIDClass -ErrorAction SilentlyContinue)
                    if (-not $hidDevices -or $hidDevices.Count -eq 0) {
                        $hidDevices = @(Get-PnpDevice -PresentOnly -ErrorAction SilentlyContinue |
                            Where-Object { $_.InstanceId -match '(?i)^HID\\VID_045E' })
                    }

                    foreach ($dev in $hidDevices) {
                        $hid = [string]$dev.InstanceId
                        if ($hid -notmatch '(?i)^HID\\VID_045E.*PID_02EA') { continue }
                        try {
                            $prop = Get-PnpDeviceProperty -InstanceId $hid -KeyName 'DEVPKEY_Device_ContainerId' -ErrorAction SilentlyContinue
                            if ($prop -and $prop.Data) {
                                $cid = ([string]$prop.Data).Trim()
                                if ($global:WatchedContainerIds.Contains($cid)) {
                                    $containerPresent.Add($cid)
                                }
                            }
                        } catch {}
                    }

                    $script:LastContainerPresentIds = @($containerPresent | Sort-Object -Unique)
                }

                foreach ($cid in $script:LastContainerPresentIds) {
                    Add-PresentControllerId $present $cid
                }
            }
        }

        if ($present.Count -eq 0) {
            $now = [datetime]::UtcNow
            if ((-not $script:HasGetPnpDevice) -or (($now - $script:LastCimFallbackUtc).TotalMilliseconds -ge $script:CimFallbackIntervalMs)) {
                $script:LastCimFallbackUtc = $now
                $fallbackPresent = [System.Collections.Generic.List[string]]::new()
                $fallback = @(Get-CimInstance Win32_PnPEntity -ErrorAction SilentlyContinue)
                foreach ($dev in $fallback) {
                    $id = [string]$dev.DeviceID
                    if (-not [string]::IsNullOrWhiteSpace($id) -and $global:WatchedControllerIds.Contains($id)) {
                        $fallbackPresent.Add($id)
                    }
                }
                $script:LastCimPresentIds = @($fallbackPresent | Sort-Object -Unique)
            }

            foreach ($id in $script:LastCimPresentIds) {
                Add-PresentControllerId $present $id
            }
        }

        @($present | Sort-Object)
    } catch {
        Write-Log "Poll error: $($_.Exception.Message)"
        @()
    }
