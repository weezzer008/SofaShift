function Get-PresentControllerIds {
    try {
        $present = [System.Collections.Generic.HashSet[string]]::new([System.StringComparer]::OrdinalIgnoreCase)

        if ($script:HasGetPnpDevice) {
            foreach ($id in $global:WatchedControllerIds) {
                try {
                    $dev = Get-PnpDevice -PresentOnly -InstanceId $id -ErrorAction SilentlyContinue
