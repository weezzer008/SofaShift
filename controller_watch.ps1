        return rc;
    }

    // Returns the GDI device name (e.g. "\\.\DISPLAY2") for the active path to the given target.
    // Called after ApplyDisplay succeeds so the display is guaranteed active.
    public static string GetActiveSourceName(uint adapterLow, int adapterHigh, uint targetId)
    {
        try {
            DISPLAYCONFIG_PATH_INFO[] paths;
            DISPLAYCONFIG_MODE_INFO[] modes;
            Query(out paths, out modes);
            foreach (var p in paths) {
                if (p.targetInfo.adapterId.LowPart == adapterLow &&
                    p.targetInfo.adapterId.HighPart == adapterHigh &&
                    p.targetInfo.id == targetId &&
