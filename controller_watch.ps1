function Convert-LegacyConfigPs1ToData {
    param([Parameter(Mandatory=$true)][string]$Path)

    $tokens = $null
    $errors = $null
    $ast = [System.Management.Automation.Language.Parser]::ParseFile($Path, [ref]$tokens, [ref]$errors)
    if ($errors -and $errors.Count -gt 0) {
        throw "Legacy config parse failed: $($errors[0].Message)"
    }

    $allowed = @{
        cfg_FRLToggle = 'FRLToggle'
        cfg_NirCmd = 'NirCmd'
        cfg_Playnite = 'Playnite'
        cfg_HueSync = 'HueSync'
        cfg_CustomApp = 'CustomApp'
        cfg_CustomAppLabel = 'CustomAppLabel'
        cfg_CustomApps = 'CustomApps'
        cfg_Profiles = 'Profiles'
        cfg_DesktopProfile = 'DesktopProfile'
        cfg_ControllerMap = 'ControllerMap'
        cfg_ControllerContainerMap = 'ControllerContainerMap'
        cfg_DebounceSeconds = 'DebounceSeconds'
        cfg_FocusTarget = 'FocusTarget'
        cfg_AudioDelayMs = 'AudioDelayMs'
        cfg_PollMs = 'PollMs'
    }

    $data = @{}
    $statements = @()
    if ($ast.EndBlock) { $statements = @($ast.EndBlock.Statements) }
    foreach ($stmt in $statements) {
        if ($stmt -isnot [System.Management.Automation.Language.AssignmentStatementAst]) {
            throw "Legacy config contains executable statement '$($stmt.Extent.Text.Trim())'. Re-run the setup wizard to create config.psd1."
        }
        if ($stmt.Left -isnot [System.Management.Automation.Language.VariableExpressionAst]) {
            throw "Legacy config contains an unsupported assignment. Re-run the setup wizard to create config.psd1."
        }

        $varName = $stmt.Left.VariablePath.UserPath
        if (-not $allowed.ContainsKey($varName)) {
            throw "Legacy config contains unsupported variable '$varName'. Re-run the setup wizard to create config.psd1."
        }
        if (-not ($stmt.Right -is [System.Management.Automation.Language.CommandExpressionAst]) -or -not $stmt.Right.Expression) {
            throw "Legacy config assignment '$varName' is not a literal value."
        }

        try {
            $data[$allowed[$varName]] = $stmt.Right.Expression.SafeGetValue()
        } catch {
            throw "Legacy config assignment '$varName' is not a safe literal value: $($_.Exception.Message)"
        }
    }
    return $data
}

function Import-SofaShiftConfigData {
    param([Parameter(Mandatory=$true)][string]$BaseDir)

    $dataPath = Join-Path $BaseDir "config.psd1"
    $legacyPath = Join-Path $BaseDir "config.ps1"

    if (Test-Path -LiteralPath $dataPath) {
        Write-Log "Loading config.psd1"
        return (Import-PowerShellDataFile -LiteralPath $dataPath)
    }

    if (Test-Path -LiteralPath $legacyPath) {
        Write-Log "Loading legacy config.ps1 as data only; executable statements are refused"
        return (Convert-LegacyConfigPs1ToData -Path $legacyPath)
    }

    throw "No config found. Expected config.psd1 at $dataPath"
}

function Set-SofaShiftConfigVariables {
    param([Parameter(Mandatory=$true)][hashtable]$Data)

    $global:cfg_FRLToggle = if ($Data.ContainsKey('FRLToggle')) { [string]$Data.FRLToggle } else { '' }
    $global:cfg_NirCmd = if ($Data.ContainsKey('NirCmd')) { [string]$Data.NirCmd } else { '' }
    $global:cfg_Playnite = if ($Data.ContainsKey('Playnite')) { [string]$Data.Playnite } else { '' }
    $global:cfg_HueSync = if ($Data.ContainsKey('HueSync')) { [string]$Data.HueSync } else { '' }
    $global:cfg_CustomApp = if ($Data.ContainsKey('CustomApp')) { [string]$Data.CustomApp } else { '' }
    $global:cfg_CustomAppLabel = if ($Data.ContainsKey('CustomAppLabel')) { [string]$Data.CustomAppLabel } else { '' }
    $global:cfg_CustomApps = if ($Data.ContainsKey('CustomApps') -and $Data.CustomApps) { @($Data.CustomApps) } else { @() }
    $global:cfg_Profiles = if ($Data.ContainsKey('Profiles') -and $Data.Profiles) { $Data.Profiles } else { @{} }
    $global:cfg_DesktopProfile = if ($Data.ContainsKey('DesktopProfile')) { [string]$Data.DesktopProfile } else { '' }
    $global:cfg_ControllerMap = if ($Data.ContainsKey('ControllerMap') -and $Data.ControllerMap) { $Data.ControllerMap } else { @{} }
    $global:cfg_ControllerContainerMap = if ($Data.ContainsKey('ControllerContainerMap') -and $Data.ControllerContainerMap) { $Data.ControllerContainerMap } else { @{} }
    $global:cfg_DebounceSeconds = if ($Data.ContainsKey('DebounceSeconds')) { [int]$Data.DebounceSeconds } else { 10 }
    $global:cfg_FocusTarget = if ($Data.ContainsKey('FocusTarget') -and $Data.FocusTarget) { [string]$Data.FocusTarget } else { 'Playnite' }
    $global:cfg_AudioDelayMs = if ($Data.ContainsKey('AudioDelayMs')) { [int]$Data.AudioDelayMs } else { 3000 }
    if ($Data.ContainsKey('PollMs')) { $global:cfg_PollMs = [int]$Data.PollMs }
}

try {
    $configData = Import-SofaShiftConfigData -BaseDir $PSScriptRoot
    Set-SofaShiftConfigVariables -Data $configData
} catch {
    Write-Log "Failed to load SofaShift config: $($_.Exception.Message)"
    exit 1
}

if (-not $cfg_ControllerMap -or $cfg_ControllerMap.Keys.Count -eq 0) {
    Write-Log "No controllers are mapped in config.psd1. Exiting."
    exit 1
}

# ============================================================
# Config cross-reference validation
# Log (but don't exit) when the config references profiles/files that don't exist.
# This makes debugging a broken setup much faster - the user sees the exact problem
# in the log instead of a silent failure to switch displays.
# ============================================================
$cfgIssues = [System.Collections.Generic.List[string]]::new()

if (-not $cfg_Profiles -or $cfg_Profiles.Keys.Count -eq 0) {
    $cfgIssues.Add("cfg_Profiles is empty - no display profiles defined")
}

if ($cfg_Profiles -and $cfg_DesktopProfile -and -not $cfg_Profiles.ContainsKey($cfg_DesktopProfile)) {
    $cfgIssues.Add("cfg_DesktopProfile '$cfg_DesktopProfile' is not a defined profile in cfg_Profiles")
}

if ($cfg_Profiles -and $cfg_ControllerMap) {
    foreach ($devId in $cfg_ControllerMap.Keys) {
        $target = $cfg_ControllerMap[$devId]
        if ($target -and -not $cfg_Profiles.ContainsKey($target)) {
            $cfgIssues.Add("Controller '$devId' maps to profile '$target' which is not defined in cfg_Profiles")
        }
    }
}

if ($cfg_Profiles) {
    foreach ($name in $cfg_Profiles.Keys) {
        $p = $cfg_Profiles[$name]
        if (-not ($p.TargetId -and [uint32]$p.TargetId -gt 0)) {
            $cfgIssues.Add("Profile '$name' has no native CCD target configured (TargetId is 0).")
        }
    }
}

# Tool paths referenced in config
if ($cfg_NirCmd -and -not (Test-Path -LiteralPath $cfg_NirCmd)) {
    $cfgIssues.Add("cfg_NirCmd not found on disk: $cfg_NirCmd")
}
if ($cfg_Playnite -and -not (Test-Path -LiteralPath $cfg_Playnite)) {
    $cfgIssues.Add("cfg_Playnite not found on disk: $cfg_Playnite")
}

if ($cfgIssues.Count -gt 0) {
    Write-Log "Config validation: $($cfgIssues.Count) issue(s) detected:"
    foreach ($issue in $cfgIssues) { Write-Log "  - $issue" }
    Write-Log "Continuing with partial config - re-run the setup wizard to fix."
} else {
    Write-Log "Config validation: OK"
}

# ============================================================
# Single-instance guard (named mutex, per-user)
# Previous version parsed Win32_Process command-lines, which is slow, needs CIM permission,
# and can misfire across renames. A session-scoped mutex is cleaner and self-cleans on exit.
# ============================================================
$global:SingleInstanceMutex = $null
try {
    $created = $false
    $mutexName = "Local\SofaShift.ControllerWatch.$($env:USERNAME)"
    $global:SingleInstanceMutex = New-Object System.Threading.Mutex($true, $mutexName, [ref]$created)
    if (-not $created) {
        Write-Log "Another SofaShift watcher is already running for this user - exiting."
        exit 0
    }
} catch {
    Write-Log "Single-instance mutex check failed ($($_.Exception.Message)) - continuing anyway."
}

Write-Log ("=" * 56)
Write-Log "Starting controller monitor"
Write-Log "Watching controllers:"
foreach ($id in $cfg_ControllerMap.Keys) {
    Write-Log "  $id  =>  $($cfg_ControllerMap[$id])"
}

# Poll interval - respect cfg_PollMs if defined in config, else default to 1000 ms.
# Clamp to a sane range so a typo in config can't melt the CPU.
$script:PollMs = 1000
if (Get-Variable -Name 'cfg_PollMs' -Scope Global -ErrorAction SilentlyContinue) {
    $candidate = 0
    if ([int]::TryParse([string]$cfg_PollMs, [ref]$candidate) -and $candidate -ge 100 -and $candidate -le 10000) {
        $script:PollMs = $candidate
    }
}
Write-Log "DebounceSeconds=$cfg_DebounceSeconds  PollMs=$script:PollMs"
Write-Log "Press Ctrl+C to stop."
Write-Log ("=" * 56)

