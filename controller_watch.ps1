function Focus-Window {
    param([string]$TitleRegex, [int]$TimeoutMs = 6000)
    $sw = [Diagnostics.Stopwatch]::StartNew()
    while ($sw.ElapsedMilliseconds -lt $TimeoutMs) {
        $hwnd = [XCS.User32]::FindWindowByTitleRegex($TitleRegex)
        if ($hwnd -ne [IntPtr]::Zero) {
            Start-Sleep -Milliseconds 150
            [XCS.User32]::SetForegroundWindow($hwnd) | Out-Null
            return $true
        }
        Start-Sleep -Milliseconds 300
    }
    return $false
}

function Resolve-FocusTarget {
    param([string]$Target)
    $value = ([string]$Target).Trim()
    if ([string]::IsNullOrWhiteSpace($value)) { return 'Playnite' }
    if ($value -match '^(?i:windows desktop|desktop)$') { return 'Windows Desktop' }
    if ($value -match '^(?i:playnite)$') { return 'Playnite' }
    Write-Log "Unknown focus target '$value'; defaulting to Playnite"
    return 'Playnite'
}

function Invoke-ConfiguredFocus {
